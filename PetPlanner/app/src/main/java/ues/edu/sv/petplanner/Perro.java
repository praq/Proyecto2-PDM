package ues.edu.sv.petplanner;

public class Perro {

    private String nombrePerro;
    private String raza;
    private String edadPerro;
    private String colorPerro;
    private float pesoPerro;

    public Perro(){

    }

    public String getNombrePerro() {
        return nombrePerro;
    }

    public void setNombrePerro(String nombrePerro) {
        this.nombrePerro = nombrePerro;
    }

    public String getRaza() {
        return raza;
    }

    public void setRaza(String raza) {
        this.raza = raza;
    }

    public String getEdadPerro() {
        return edadPerro;
    }

    public void setEdadPerro(String edadPerro) {
        this.edadPerro = edadPerro;
    }

    public String getColorPerro() {
        return colorPerro;
    }

    public void setColorPerro(String colorPerro) {
        this.colorPerro = colorPerro;
    }

    public float getPesoPerro() {
        return pesoPerro;
    }

    public void setPesoPerro(float pesoPerro) {
        this.pesoPerro = pesoPerro;
    }
}
